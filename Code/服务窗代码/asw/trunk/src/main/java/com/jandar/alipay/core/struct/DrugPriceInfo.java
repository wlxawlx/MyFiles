public class DrugPriceInfo {
    private String yplx;//     药品类型
    private String ypmc;//   药品名称
    private String ypdw;//  计量单位
    private String ypgg;//    药品规格
    private String ypcd;//   药品产地
    private String ypjg;//  药品价格

    public String getYplx() {
        return yplx;
    }

    public void setYplx(String yplx) {
        this.yplx = yplx;
    }

    public String getYpmc() {
        return ypmc;
    }

    public void setYpmc(String ypmc) {
        this.ypmc = ypmc;
    }

    public String getYpdw() {
        return ypdw;
    }

    public void setYpdw(String ypdw) {
        this.ypdw = ypdw;
    }

    public String getYpgg() {
        return ypgg;
    }

    public void setYpgg(String ypgg) {
        this.ypgg = ypgg;
    }

    public String getYpcd() {
        return ypcd;
    }

    public void setYpcd(String ypcd) {
        this.ypcd = ypcd;
    }

    public String getYpjg() {
        return ypjg;
    }

    public void setYpjg(String ypjg) {
        this.ypjg = ypjg;
    }
}
